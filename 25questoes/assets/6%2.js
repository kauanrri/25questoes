console.log(6 % 2); // 0

//Cálculo de 6 % 2:
//Divisão de 6 por 2: O quociente é 3, e o resto é 0 (porque 2 × 3 = 6 e 6 - 6 = 0).
//Portanto, 6 % 2 = 0.
//2. O que isso indica sobre o número 6:
//O fato de o resto ser 0 indica que 6 é divisível por 2.
//Isso significa que 6 é um número par, pois números pares são aqueles que, quando divididos por 2, não deixam resto.