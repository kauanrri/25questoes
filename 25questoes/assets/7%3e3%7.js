console.log(7 % 3); // 1
console.log(3 % 7); // 3
//. Cálculo de 7 % 3:
//Divisão de 7 por 3: O quociente é 2, e o resto é 1 (porque 3 × 2 = 6 e 7 - 6 = 1).
//Portanto, 7 % 3 = 1.
//2. Cálculo de 3 % 7:
//Divisão de 3 por 7: O quociente é 0, e o resto é 3 (porque 3 não é divisível por 7 e o valor restante é o próprio 3).
//Portanto, 3 % 7 = 3.